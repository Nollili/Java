    public void FileKiir(String fileName, String text1, String text2, String text3){
       try{
        FileWriter fw = new FileWriter(fileName, true);
        if(jPanel1.isEnabled() == true){
            fw.write(text1);
            fw.write(text2);
            fw.write(text3);
        }else if(jPanel2.isEnabled() == true){
            fw.write(text1);
            fw.write(text2);
            fw.write(text3);
        }else if(jPanel3.isEnabled() == true){
            fw.write(text1);
            fw.write(text2);
            fw.write(text3);
        }       
        fw.close();
       }catch (IOException ex) {
       }
    }
    
    
