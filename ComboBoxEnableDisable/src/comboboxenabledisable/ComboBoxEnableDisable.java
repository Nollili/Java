    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if(jComboBox1.getSelectedItem().equals("Personal")){
            
            jPanel1.setEnabled(true);
            jPanel2.setEnabled(false);
            jPanel3.setEnabled(false);
            
            jTextField1.setEnabled(true);
            jTextField2.setEnabled(true);
            jTextField3.setEnabled(true);
            
            jTextField4.setEnabled(false);
            jTextField5.setEnabled(false);
            jTextField6.setEnabled(false);
            
            jTextField7.setEnabled(false);
            jTextField8.setEnabled(false);
            jTextField9.setEnabled(false);
            
        } else if (jComboBox1.getSelectedItem().equals("Salary")){
            
            jPanel1.setEnabled(false);
            jPanel2.setEnabled(true);
            jPanel3.setEnabled(false);
            
            jTextField1.setEnabled(false);
            jTextField2.setEnabled(false);
            jTextField3.setEnabled(false);
            
            jTextField4.setEnabled(true);
            jTextField5.setEnabled(true);
            jTextField6.setEnabled(true);
            
            jTextField7.setEnabled(false);
            jTextField8.setEnabled(false);
            jTextField9.setEnabled(false);
            
        } else if (jComboBox1.getSelectedItem().equals("Title")){
            
            jPanel1.setEnabled(false);
            jPanel2.setEnabled(false);
            jPanel3.setEnabled(true);
            
            jTextField1.setEnabled(false);
            jTextField2.setEnabled(false);
            jTextField3.setEnabled(false);
            
            jTextField4.setEnabled(false);
            jTextField5.setEnabled(false);
            jTextField6.setEnabled(false);
            
            jTextField7.setEnabled(true);
            jTextField8.setEnabled(true);
            jTextField9.setEnabled(true);
            
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jPanel1.isEnabled() == true){
            FileKiir("Personal.txt", jTextField1.getText(), jTextField2.getText(),jTextField3.getText());
        }else if (jPanel2.isEnabled() == true){
            FileKiir("Salary.txt", jTextField4.getText(), jTextField5.getText(),jTextField6.getText());
        }else if (jPanel3.isEnabled() == true){
            FileKiir("Title.txt", jTextField7.getText(), jTextField8.getText(),jTextField9.getText());
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

